                        <h2>Heart Surgery</h2>
                        <p style="text-align:justify;">Heart-related problems do not always require surgery. 
                            Sometimes they can be addressed with lifestyle changes, medications, or nonsurgical procedures.
                             For example, catheter ablation uses energy to make small scars in your 
                             heart tissue to prevent abnormal electrical signals from moving through your heart. 
                             Coronary angioplasty is a minimally invasive procedure in which a stent is 
                             inserted into a narrowed or blocked coronary artery to hold it open. Nonetheless, 
                             surgery is often needed to address problems such as heart failure, 
                             plaque buildup that partially or totally blocks blood flow in a coronary artery, 
                             faulty heart valves, dilated or diseased major blood vessels (such as the aorta), 
                             and abnormal heart rhythms.</p>
