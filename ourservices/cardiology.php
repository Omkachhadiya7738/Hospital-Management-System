                        <h2>What are the risks?</h2>
                        <p>Most heart surgeries are major surgeries. Although often successful, they do entail risks. The National Heart, Lung, and Blood Institute identifies some of these risks as:</p>
                        <ul>
                            <li>Bleeding</li>
                            <li>Infection</li>
                            <li>Reactions to anesthesia</li>
                            <li>Damage to tissues in the heart, kidneys, liver, and lungs</li>
                            <li>Stroke</li>
                            <li>Death, especially for someone who is already very sick before surgery</li>
                        </ul>
                        <p>The risk is higher if you have other diseases or conditions, such as diabetes, peripheral artery disease, or kidney or lung disease.</p>
