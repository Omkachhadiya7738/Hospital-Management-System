                <div class="col-12 col-lg-12 col-md-12 mt-3">
                        <h2>What are some types of heart surgery?</h2>
                        <p style="text-align:justify;">There are many types of heart surgery. The National Heart, Lung, 
                            and Blood Institute, which is part of the National Institutes of Health, 
                            lists the following as among the most common coronary surgical procedures.</p>
                
                        <ul>
                            <li style="text-align:justify;">Coronary artery bypass grafting (CABG). In CABG — the most common type of heart surgery — the surgeon takes a healthy artery or vein from elsewhere in your body and connects it to supply blood past the blocked coronary artery. The grafted artery or vein bypasses the blocked portion of the coronary artery, creating a new path for blood to flow to the heart muscle. Often, this is done for more than one coronary artery during the same surgery. CABG is sometimes referred to as heart bypass or coronary artery bypass surgery.</li>
                            <li style="text-align:justify;">Heart valve repair or replacement. Surgeons either repair the valve or replace it with an artificial valve or with a biological valve made from pig, cow, or human heart tissue. One repair option is to insert a catheter through a large blood vessel, guide it to the heart, and inflate and deflate a small balloon at the tip of the catheter to widen a narrow valve.</li>
                            <li style="text-align:justify;">Insertion of a pacemaker or an implantable cardioverter defibrillator (ICD). Medicine is usually the first treatment option for arrhythmia, a condition in which the heart beats too fast, too slow or with an irregular rhythm. If medication does not work, a surgeon may implant a pacemaker under the skin of the chest or abdomen, with wires that connect it to the heart chambers. The device uses electrical pulses to control the heart rhythm when a sensor detects that it is abnormal. An ICD works similarly, but it sends an electric shock to restore a normal rhythm when it detects a dangerous arrhythmia.</li>
                            <li style="text-align:justify;">Maze surgery. The surgeon creates a pattern of scar tissue within the upper chambers of the heart to redirect electrical signals along a controlled path to the lower heart chambers. The surgery blocks the stray electrical signals that cause atrial fibrillation — the most common type of serious arrhythmia.</li>
                            <li style="text-align:justify;">Aneurysm repair. A weak section of the artery or heart wall is replaced with a patch or graft to repair a balloon-like bulge in the artery or wall of the heart muscle.</li>
                            <li style="text-align:justify;">Heart transplant. The diseased heart is removed and replaced with a healthy heart from a deceased donor.</li>
                            <li style="text-align:justify;">Insertion of a ventricular assist device (VAD) or total artificial heart (TAH). A VAD is a mechanical pump that supports heart function and blood flow. A TAH replaces the two lower chambers of the heart.</li>
                        </ul>
                
                        <p style="text-align:justify;">In addition to these surgeries, a minimally invasive alternative to open-heart surgery that is becoming more common is transcatheter structural heart surgery. This involves guiding a long, thin, flexible tube called a catheter to your heart through blood vessels that can be accessed from the groin, thigh, abdomen, chest, neck, or collarbone. A small incision is necessary. This type of surgery includes transcatheter aortic valve implantation to replace a faulty aortic valve with a valve made from animal tissue, MitraClip® placement for mitral valve abnormalities, and WATCHMAN® placement for nonvalvular atrial fibrillation patients.</p>
